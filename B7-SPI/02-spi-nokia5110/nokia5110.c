static void nokia5110_send(unsigned char data, unsigned char is_data)
{
    gpio_set_value(ce_pin, 0);
    gpio_set_value(dc_pin, is_data);

    spi_write(nokia5110_spi, &data, 1);

    gpio_set_value(ce_pin, 1);
}

static void nokia5110_write_char(char c)
{
    int i;
    for (i = 0; i < 5; i++)
        nokia5110_send(ASCII[c - 0x20][i], LCD_DATA);
    nokia5110_send(0x00, LCD_DATA);
}

static void nokia5110_write_string(const char *str)
{
    while (*str)
        nokia5110_write_char(*str++);
}

static void nokia5110_clear_screen(void)
{
    int i;
    for (i = 0; i < (LCD_WIDTH * LCD_HEIGHT / 8); i++)
        nokia5110_send(0x00, LCD_DATA);
}

static void nokia5110_init(void)
{
    gpio_set_value(re_pin, 0);
    mdelay(10);
    gpio_set_value(re_pin, 1);

    nokia5110_send(0x21, LCD_CMD);
    nokia5110_send(0xB0, LCD_CMD);
    nokia5110_send(0x04, LCD_CMD);
    nokia5110_send(0x14, LCD_CMD);

    nokia5110_send(0x20, LCD_CMD);
    nokia5110_send(0x0C, LCD_CMD);
}

static int nokia5110_probe(struct spi_device *spi)
{
    int ret;

    ret = gpio_request(re_pin, "RE");
    if (ret) {
        pr_err("Failed to request GPIO for RE pin\n");
        return ret;
    }

    ret = gpio_request(dc_pin, "DC");
    if (ret) {
        pr_err("Failed to request GPIO for DC pin\n");
        gpio_free(re_pin);
        return ret;
    }

    ret = gpio_request(ce_pin, "CE");
    if (ret) {
        pr_err("Failed to request GPIO for CE pin\n");
        gpio_free(re_pin);
        gpio_free(dc_pin);
        return ret;
    }

    ret = gpio_request(bl_pin, "BL");
    if (ret) {
        pr_err("Failed to request GPIO for BL pin\n");
        gpio_free(re_pin);
        gpio_free(dc_pin);
        gpio_free(ce_pin);
        return ret;
    }

    gpio_direction_output(re_pin, 0);
    gpio_direction_output(dc_pin, 0);
    gpio_direction_output(ce_pin, 0);
    gpio_direction_output(bl_pin, 1); // Bật đèn nền

    nokia5110_spi = spi;
    nokia5110_init();
    nokia5110_clear_screen();
    nokia5110_write_string("Hello, World!!!");

    return 0;
}

static int nokia5110_remove(struct spi_device *spi)
{
    nokia5110_clear_screen();

    gpio_free(re_pin);
    gpio_free(dc_pin);
    gpio_free(ce_pin);
    gpio_free(bl_pin);

    return 0;
}

